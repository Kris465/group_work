# Ввод натурального числа n
n = int(input("Введите натуральное число n (n ≤ 9999): "))

if n > 9999:
    print("Ошибка: число должно быть ≤ 9999")
else:
    # Преобразуем в строку и дополняем нулями слева до 4 символов
    s = str(n).zfill(4)
    
    # Преобразуем строку в множество (дубликаты удалятся)
    unique_digits = set(s)
    
    # Если длина множества равна 4, то все цифры различны
    if len(unique_digits) == 4:
        print(f"Да, в числе {n} (запись '{s}') все четыре цифры различны")
    else:
        print(f"Нет, в числе {n} (запись '{s}') не все цифры различны")