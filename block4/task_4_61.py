# Ввод натурального числа n
n = int(input("Введите натуральное число n (n ≤ 9999): "))

# Проверка ограничения
if n > 9999:
    print("Ошибка: число должно быть ≤ 9999")
else:
    # Преобразуем в строку и дополняем нулями слева до 4 символов
    s = str(n).zfill(4)
    
    # Проверка: является ли строка палиндромом
    if s == s[::-1]:
        print(f"Да, число {n} (запись '{s}') является палиндромом")
    else:
        print(f"Нет, число {n} (запись '{s}') не является палиндромом")