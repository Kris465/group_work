# Ввод числа
num = int(input("Введите четырехзначное число: "))

# Проверка, что число действительно четырехзначное
if 1000 <= num <= 9999:
    # Преобразуем число в строку для удобной проверки
    str_num = str(num)
    
    # Пункт а: входит ли цифра 4
    if '4' in str_num:
        print("а) Цифра 4 входит в число")
    else:
        print("а) Цифра 4 не входит в число")
    
    # Пункт б: входит ли цифра b
    b = input("Введите цифру b для проверки: ")
    # Проверяем, что введен один символ и это цифра
    if len(b) == 1 and b.isdigit():
        if b in str_num:
            print(f"б) Цифра {b} входит в число")
        else:
            print(f"б) Цифра {b} не входит в число")
    else:
        print("Ошибка: нужно ввести одну цифру")
else:
    print("Ошибка: число должно быть четырехзначным")